export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getProductsByStore(store: StoreCategory): Product[] {
  return products.filter((p) => p.store === store);
}

export function getBestsellersByStore(store: StoreCategory, limit = 4): Product[] {
  return [...getProductsByStore(store)]
    .sort((a, b) => b.sold - a.sold)
    .slice(0, limit);
}
